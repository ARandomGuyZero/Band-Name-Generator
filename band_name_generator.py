"""
Band Name Generator

This script generates a band name based on the user's inputs: the city they grew up in and the name of their pet.
"""

# Welcome message
print("Welcome to the Band Name Generator")

# Get the user's city
city = input("What's the city you grew up in?\n")

# Get the user's pet name
pet = input("What's the name of your pet?\n")

# Generate and display the band name
print("Your band name is " + city + " " + pet)
